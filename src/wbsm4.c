void wbsm4_gen(uint8_t *key)
{
    int i, j, x;
    Aff32 P[36];
    Aff32 P_inv[36];
    Aff8 Eij[32][4];
    Aff8 Eij_inv[32][4];
    Aff32 Ei_inv[32];
    Aff8 Qij[32][4];
    Aff8 Qij_inv[32][4];
    Aff64 Q[32];
    Aff32 Q_inv[32];
    uint8_t temp_u8;
    uint16_t temp_u16;
    uint32_t temp_u32;
    uint64_t temp_u64;

    sm4_context ctx;
    sm4_setkey_enc(&ctx, key);
    InitRandom(((unsigned int)time(NULL)));

    for (i = 0; i < 36; i++) 
    {
        //affine P
        genaffinepairM32(&P[i], &P_inv[i]);
    }

    for (i = 0; i < 32; i++) 
    {
        //affine E
        for (j = 0; j < 4; j++) 
        {
            genaffinepairM8(&Eij[i][j], &Eij_inv[i][j]);
            genaffinepairM8(&Qij[i][j], &Qij_inv[i][j]);
        }

        // combine 4 E8 to 1 E32
        affinecomM8to32(Eij_inv[i][0], Eij_inv[i][1], Eij_inv[i][2], Eij_inv[i][3], &Ei_inv[i]);

        //affine M
        affinemixM32(Ei_inv[i], P_inv[i + 1], &M[i][0]);
        affinemixM32(Ei_inv[i], P_inv[i + 2], &M[i][1]);
        affinemixM32(Ei_inv[i], P_inv[i + 3], &M[i][2]);

        //affine Q
        affinecomM8to64(Qij[i][0], Qij[i][1], Qij[i][2], Qij[i][3], Qij[i][0], Qij[i][1], Qij[i][2], Qij[i][3], &Q[i]);
        affinecomM8to32(Qij_inv[i][0], Qij_inv[i][1], Qij_inv[i][2], Qij_inv[i][3], &Q_inv[i]);

        //affine C D, C for Xi0, D for T(Xi1+Xi2+Xi3+rk)
        affinemixM32(P[i + 4], P_inv[i], &C[i]);
        affinemixM32(P[i + 4], Q_inv[i], &D[i]);
        temp_u32 = cus_random();
        C[i].Vec.V ^= temp_u32;
        D[i].Vec.V ^= P[i + 4].Vec.V ^ temp_u32;
    }

    for (i = 0; i < 32; i++)
    {
        V64 Q_constant[3];
        for(j = 0; j < 3; j++)
        {
            randV64(&Q_constant[j]);
        }

        uint8_t randnum[4];
        V8 randvec[4];
        for(j = 0; j < 4; j++)
        {
            randnum[j] = cus_random() % 2;
            randV8(&randvec[j]);
        }

        for (x = 0; x < 256; x++) 
        {
            for (j = 0; j < 4; j++) 
            {
                temp_u8 = affineU8(Eij[i][j], x);
                if(randnum[j] == 0) temp_u16 = (SBOX[temp_u8 ^ ((ctx.sk[i] >> (24 - j * 8)) & 0xff)] << 8) | SBOX[temp_u8 ^ randvec[j].V];
                else temp_u16 = (SBOX[temp_u8 ^ randvec[j].V] << 8) | SBOX[temp_u8 ^ ((ctx.sk[i] >> (24 - j * 8)) & 0xff)];
                temp_u64 = ((uint64_t) temp_u16) << (48 - 16 * j);
                temp_u64 = MatMulNumM64(L_matrix, temp_u64);
                if(randnum[j] == 0) temp_u64 = MatMulNumM64(SR0, temp_u64);
                else temp_u64 = MatMulNumM64(SR1, temp_u64);
                Table[i][j][x] = MatMulNumM64(Q[i].Mat, temp_u64);
            }
            for(j = 0; j < 3; j++)
            {
                Table[i][j][x] ^= Q_constant[j].V;
            }
            Table[i][3][x] ^=  Q[i].Vec.V ^ Q_constant[0].V ^ Q_constant[1].V ^ Q_constant[2].V;
        }
    }

    //external encoding
    for (i = 0; i < 4; i++) 
    {
        SE[i].Mat = P[i].Mat;
        SE[i].Vec = P[i].Vec;

        FE[i].Mat = P_inv[35 - i].Mat;
        FE[i].Vec = P_inv[35 - i].Vec;
    }
}

void wbsm4_encrypt(unsigned char IN[], unsigned char OUT[])
{
    int i;
    uint32_t x0, x1, x2, x3, x4;
    uint64_t xx4;
    uint32_t xt0, xt1, xt2, xt3, xt4;
    
    x0 = GET32(IN);
    x1 = GET32(IN + 4);
    x2 = GET32(IN + 8);
    x3 = GET32(IN + 12);

    for(i = 0; i < 32; i++)
    {
        xt1 = affineU32(M[i][0], x1);
        xt2 = affineU32(M[i][1], x2);
        xt3 = affineU32(M[i][2], x3);
        x4 = xt1 ^ xt2 ^ xt3;
        xx4 = Table[i][0][(x4 >> 24) & 0xff] ^ Table[i][1][(x4 >> 16) & 0xff] ^ Table[i][2][(x4 >> 8) & 0xff] ^ Table[i][3][x4 & 0xff];
        x4 = xx4 >> 32;
        xt0 = affineU32(C[i], x0);
        xt4 = affineU32(D[i], x4);
        x4 = xt0 ^ xt4;
        
        x0 = x1;
        x1 = x2;
        x2 = x3;
        x3 = x4;
    }

    PUT32(x3, OUT);
    PUT32(x2, OUT + 4);
    PUT32(x1, OUT + 8);
    PUT32(x0, OUT + 12);
}